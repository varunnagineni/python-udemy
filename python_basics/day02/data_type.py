# Strings
print("123" + "456")

# Integer
print(123 + 456)

# Large Integers
print(123_456_789)

# Floating numbers
print(3.143)

# Boolean
print(True)
print(False)

# Convert Integer to String to get the length of the integer
print(len(str(12345)))

# Type function to know the type of the provided value
print(type("Hello"))
print(type(1234))
print(type(True))
print(type(3.143))

# Type conversion or Type casting: Converting from one type to another
print(int("1234") + int("4566"))